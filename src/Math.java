public class Math {
    public static int a;
    public static int b;
    public static int c;
    public static int d;

    public static int summ(int a, int b){
        return a + b;
    }

    public static int bigSumm(int a, int b, int c, int d){
        return a + b + c + d;
    }

    public static int minus(int a, int b){
        return a - b;
    }

}
